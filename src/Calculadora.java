public class Calculadora {
    public static void main(String[] args) {
        int A = 10;
        int B = 5;

        System.out.println("A multiplicação dos números é de: " + (A * B));
        System.out.println("A divisão dos números é de: " + (A / B));
        System.out.println("A soma dos números é de: " + (A + B));
        System.out.println("A subtração dos números é de: " + (A - B));
    }
}

